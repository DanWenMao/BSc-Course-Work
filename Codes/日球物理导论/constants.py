"""
constants.py — 物理常数与模型参数

所有量采用 cgs 单位制，天文学常用单位 (AU, yr, G) 标出。
"""

import numpy as np

# ============================================================================
# 物理常数 (cgs)
# ============================================================================
G = 6.67430e-8          # 引力常数            cm³/(g·s²)
kB = 1.380649e-16       # Boltzmann 常数      erg/K
mH = 1.6735575e-24      # 氢原子质量          g
sigma_SB = 5.670367e-5  # Stefan–Boltzmann    erg/(cm²·s·K⁴)

# ============================================================================
# 太阳参数
# ============================================================================
M_sun = 1.989e33        # 太阳质量            g
R_sun = 6.957e10        # 太阳半径            cm
L_sun = 3.828e33        # 太阳光度            erg/s

# ============================================================================
# 天文单位
# ============================================================================
AU = 1.495978707e13     # cm
yr = 3.15576e7          # s

# ============================================================================
# 年轻恒星 (T Tauri) 模型参数
# ============================================================================
M_star = 1.0 * M_sun    # 恒星质量
R_star = 2.0 * R_sun    # 年轻太阳半径 (~2 R⊙, 尚未收缩到主序)
B_star = 3000.0         # 表面偶极磁场强度   G
M_dot   = 2.0e-9        # 盘吸积率           M⊙/yr

# ---------------------------------------------------------------------------
# X-point 计算中使用的无量纲常数 k (量级 ~1, 可调)
# 增大 k → X-point 向外移动
# ---------------------------------------------------------------------------
k_X = 0.35

# ---------------------------------------------------------------------------
# 恒星自转角速度 (满足 R_X ≈ R_co 的共转条件)
# 将在 xpoint.py 中由 R_X 反推或直接设定
# ---------------------------------------------------------------------------
Omega_star = None       # 运行时由 xpoint.py 计算

# ============================================================================
# 风参数 (等温假设)
# ============================================================================
mu_mol = 2.3            # 平均分子量 (太阳组成, 部分电离)
T_wind = 3000.0         # 等温风温度         K
c_s = np.sqrt(kB * T_wind / (mu_mol * mH))  # 等温声速  cm/s

# ============================================================================
# CAI 颗粒参数
# ============================================================================
rho_CAI = 3.0           # CAI 固体密度       g/cm³
a_CAI_min = 0.01        # 最小粒径           cm
a_CAI_max = 1.0         # 最大粒径           cm
n_CAI = 20              # 模拟颗粒数

# ============================================================================
# 数值参数
# ============================================================================
n_theta = 500           # 沿磁力线的极角分点数
theta_min = np.deg2rad(2.0)  # 磁力线最小极角 (接近极轴)
n_field_lines = 6       # 开放磁力线条数（过多会触发声速点查找器数值不稳定）
n_field_lines_dipole = 12  # 背景偶极磁力线条数

# ============================================================================
# 积分参数
# ============================================================================ 
t_end = 1 * yr         # CAI 积分总时间      s
dt_output = 0.001 * yr   # 输出间隔            s (~8.8 h, 200帧→10s@20fps)
rtol = 1e-8             # ODE 相对容差
atol = 1e-10            # ODE 绝对容差
